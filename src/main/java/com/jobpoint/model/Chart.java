package com.jobpoint.model;

public class Chart {
	private double arrow_green;
	private double arrow_red;
	private double bar;
	
	public double getArrowGreen() {
		return arrow_green;
	}
	
	public void setArrowGreen(double arrow_green) {
		this.arrow_green = arrow_green;
	}
	
	public double getArrowRed() {
		return arrow_red;
	}
	
	public void setArrowRed(double arrow_red) {
		this.arrow_red = arrow_red;
	}
	
	public double getBar() {
		return bar;
	}
	
	public void setBar(double bar) {
		this.bar = bar;
	}
}
